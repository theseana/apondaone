# count, endswith, startswith, find, rfind, index, rindex


a = "Adipisicing do enim mollit laboris ex. Eiusmod aute do duis non sint cillum duis eu do. Amet laborum sint excepteur voluptate commodo commodo et anim duis aliquip nostrud."
print(a.count('i'))
print(a.count('do'))

print(a.startswith('Adipi'))
print(a.endswith('ud.'))
#############################
print(a.find('i'))          #
print(a.find('do'))         #
                            # similar to each other
print(a.index('i'))         #
print(a.index('do'))        #
#############################
print(a.rfind('i'))
print(a.rfind('do'))
